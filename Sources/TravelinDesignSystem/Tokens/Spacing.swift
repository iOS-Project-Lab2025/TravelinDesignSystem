import CoreGraphics

public extension DesignTokens {
    
    enum Spacing {
        /// 4 pt
        public static let extraSmall: CGFloat = 4.0
        /// 8 pt
        public static let small: CGFloat = 8.0
        /// 12 pt
        public static let mediumSmall: CGFloat = 12.0
        /// 16 pt (Standard margin/padding)
        public static let medium: CGFloat = 16.0
        /// 24 pt
        public static let mediumLarge: CGFloat = 24.0
        /// 32 pt
        public static let large: CGFloat = 32.0
        /// 48 pt
        public static let extraLarge: CGFloat = 48.0
        
        /// 17 pt (Vertical padding for standard buttons)
        public static let buttonVertical: CGFloat = 17.0
        
        /// 20 pt (Horizontal padding for standar buttons)
        public static let buttonHorizontal: CGFloat = 20.0
        
        /// 15 pt (Horizontal padding for small buttons)
        public static let buttonHorizontalSmall: CGFloat = 15.0
        
        /// 6 pt (Icon spacing for small buttons)
        public static let iconSpacingSmall: CGFloat = 6.0
        
        /// 10 pt (Icon spacing for menu buttons)
        public static let iconSpacingMenu: CGFloat = 10.0
    }
    
}
